'''-------Operadores de atribuição-------

Operador (=): Atribui um valor a uma variável


'''
x = 100;
print(x);

'''Operador (+=): Realiza a soma de uma variável e, em seguida, atribui o valor.'''

x += 10;
print(x);

'''Operador (-=): Realiza a subtração de uma variável e, em seguida, atribui o valor.'''

x-=60;
print(x);

'''Operador (*=): Realiza a multiplicação de uma variável e, em seguida, atribui o valor.'''

x*= 2;
print(x);

'''Operador (/=): Realiza a divisão de uma variável e, em seguida, atribui o valor.'''
x /= 4;
print(f"{x:.0f}");

'''Operador (%=): Realiza o resto da divisão de uma variável e, em seguida, atribui o valor.'''
x %= 5;
print(f"{x:.0f}");

